package com.learn.day1;

public class ComparisonOperators {
	public static void main(String[] args) {
		//ComparisonOperators  ==, > ,<, !=, >=, <=
		
		int x= 10; int y =20;
		
		System.out.println(x == y); //F
		System.out.println(x > y); //F
		System.out.println(x < y); //T
		System.out.println(x >= y); //F
		System.out.println(x <= y); //T
		System.out.println(x != y); //T
	}
}
